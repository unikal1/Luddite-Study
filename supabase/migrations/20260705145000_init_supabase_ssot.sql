  after insert on public.documents
  for each row execute function app_private.create_document_version();

drop trigger if exists documents_version_update on public.documents;
create trigger documents_version_update
  after update of title, summary, path, tags, body on public.documents
  for each row
  when (
    old.title is distinct from new.title
    or old.summary is distinct from new.summary
    or old.path is distinct from new.path
    or old.tags is distinct from new.tags
    or old.body is distinct from new.body
  )
  execute function app_private.create_document_version();

create or replace function app_private.add_activity_event()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  actor uuid;
  summary_text text;
begin
  actor := app_private.current_member_id();

  if tg_table_name = 'documents' then
    summary_text := case tg_op
      when 'INSERT' then '문서 생성: ' || new.title
      when 'UPDATE' then '문서 수정: ' || new.title
      when 'DELETE' then '문서 삭제: ' || old.title
    end;
  elsif tg_table_name = 'study_sessions' then
    summary_text := case tg_op
      when 'INSERT' then '회차 생성: ' || new.title
      when 'UPDATE' then '회차 수정: ' || new.title
      when 'DELETE' then '회차 삭제: ' || old.title
    end;
  else
    summary_text := tg_table_name || ' ' || tg_op;
  end if;

  insert into public.activity_events (actor_member_id, event_type, entity_table, entity_id, summary)
  values (
    actor,
    lower(tg_op),
    tg_table_name,
    case when tg_op = 'DELETE' then old.id::text else new.id::text end,
    summary_text
  );

  return coalesce(new, old);
end;
$$;

