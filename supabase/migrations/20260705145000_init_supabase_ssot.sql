create table if not exists public.study_sessions (
  id bigint generated by default as identity primary key,
  title text not null,
  week integer not null unique check (week > 0),
  status public.session_status not null default 'planned',
  starts_on date not null,
  ends_on date not null,
  presentation_on date not null,
  start_time time not null default '20:00',
  end_time time not null default '21:30',
  location text not null default 'Discord',
  goal text not null default '',
  facilitator_member_id uuid references public.study_members(id) on delete set null,
  presenter_member_ids uuid[] not null default '{}',
  agenda text[] not null default '{}',
  resources text[] not null default '{}',
  progress_label text not null default '진도',
  progress_current integer not null default 0 check (progress_current >= 0),
  progress_target integer not null default 1 check (progress_target > 0),
  progress_unit text not null default '개',
  created_by uuid references public.study_members(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint study_sessions_date_range check (starts_on <= ends_on and presentation_on between starts_on and ends_on),
  constraint study_sessions_progress_range check (progress_current <= progress_target)
