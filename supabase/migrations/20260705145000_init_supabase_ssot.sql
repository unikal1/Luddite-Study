create table if not exists public.study_members (
  id uuid primary key default gen_random_uuid(),
  profile_id uuid unique references public.profiles(id) on delete set null,
  member_uid text not null unique,
  display_name text not null,
  invite_email text unique,
  github_username text unique,
  role public.study_member_role not null default 'member',
  role_label text not null default '스터디원',
  color text not null default '#0f766e',
  active boolean not null default true,
  material_root text not null,
  presentation_root text not null,
