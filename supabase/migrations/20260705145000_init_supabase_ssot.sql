create or replace function app_private.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  preferred_name text;
  preferred_github text;
begin
  preferred_name := coalesce(
    new.raw_user_meta_data ->> 'name',
    new.raw_user_meta_data ->> 'full_name',
    new.raw_user_meta_data ->> 'user_name',
    split_part(coalesce(new.email, '새 사용자'), '@', 1)
  );
  preferred_github := coalesce(
    new.raw_user_meta_data ->> 'user_name',
    new.raw_user_meta_data ->> 'preferred_username'
  );

  insert into public.profiles (id, email, display_name, github_username, avatar_url)
  values (
    new.id,
    new.email,
    preferred_name,
    preferred_github,
    new.raw_user_meta_data ->> 'avatar_url'
  )
  on conflict (id) do update
  set email = excluded.email,
      display_name = excluded.display_name,
      github_username = excluded.github_username,
      avatar_url = excluded.avatar_url,
      updated_at = now();

  update public.study_members
  set profile_id = new.id,
      updated_at = now()
  where profile_id is null
    and invite_email is not null
    and lower(invite_email) = lower(coalesce(new.email, ''));

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert or update of email, raw_user_meta_data on auth.users
  for each row execute function app_private.handle_new_auth_user();

create or replace function app_private.create_document_version()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  next_version integer;
begin
  select coalesce(max(version_number), 0) + 1
  into next_version
  from public.document_versions
  where document_id = new.id;

  insert into public.document_versions (
    document_id,
    version_number,
    title,
    summary,
    path,
    tags,
    body,
    edited_by
  )
  values (
    new.id,
    next_version,
    new.title,
    new.summary,
    new.path,
    new.tags,
    new.body,
    coalesce(new.updated_by, new.created_by)
  );

  return new;
end;
$$;

drop trigger if exists documents_version_insert on public.documents;
